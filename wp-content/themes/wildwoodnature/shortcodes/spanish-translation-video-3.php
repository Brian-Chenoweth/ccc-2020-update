            <p>Así que yo pienso que es muy especial tener una área de juego que siempre está cambiando naturalmente.</p>

            <p>Otro aspecto poco común es que nosotros no tenemos un currículum de estudio sólido. Los niños y los maestros trabajan juntos cada año decidiendo que es lo que van a invertir aprendiendo.</p>

